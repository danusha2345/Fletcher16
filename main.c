#include <stdio.h>
#include <stdint.h>

uint16_t fletcher16(const uint8_t *data, size_t length) {
    uint8_t sum1 = 0;
    uint8_t sum2 = 0;

    for (size_t i = 0; i < length; i++) {
        sum1 += data[i];
        sum2 += sum1;
    }

    return (sum2 << 8) | sum1;
}

int main() {
    uint8_t data[] = {
        0x06, 0x12, 0x25, 0x00, 0x88, 0xA5, 0x2E, 0x17, 0x13, 0x08, 0xE8, 0x07, 0x08, 0x01, 0x0C, 0x01,
        0x37, 0x77, 0x97, 0x2B, 0xBD, 0x35, 0x9B, 0x39, 0x3C, 0xEE, 0x2D, 0x3B, 0xBD, 0x05, 0x00, 0x00,
        0x00, 0x05, 0x00, 0x00, 0x00, 0x0A, 0x00, 0x00, 0x00
    };
    size_t length = sizeof(data) / sizeof(data[0]);

    uint16_t checksum = fletcher16(data, length);

    printf("Fletcher16 Checksum: 0x%04X\n", checksum);

    return 0;
}